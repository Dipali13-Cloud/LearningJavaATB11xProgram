package ex_01_Java_Basic;

public class MyClass {
    public static void main(String[] args) {
        System.out.println("A class name can start with a letter (A-Z or a-z), an underscore (_), or a dollar sign ($).");
        System.out.println("A class name cannot start with a number (ex. 1MyClass is invalid)");
        System.out.println("A class name cannot contain spaces.");
        System.out.println("By convention, Java class names should follow PascalCase (e.g., MyClass).");
        System.out.println("While $ and _ are allowed, they are not recommended for class names.");
    }
}
