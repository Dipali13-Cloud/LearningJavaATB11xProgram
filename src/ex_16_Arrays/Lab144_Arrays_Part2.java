package ex_16_Arrays;

public class Lab144_Arrays_Part2 {
    public static void main(String[] args) {
        String[] names = {"Dipa","Kanchan","Nutan"};
        System.out.println(names.length);
        System.out.println(names[0]);
        System.out.println(names[1]);
        System.out.println(names[2]);

        String[] names1 = new String[3];
        names1[0] = "Priya";
        names1[1] = "Sujata";
        names1[2] = "Shravani";
        System.out.println(names1.length);
        System.out.println(names1[0]);
        System.out.println(names1[1]);
        System.out.println(names1[2]);

        Boolean[] gen = new Boolean[2];
        gen[0] = true;
        gen[1] = false;
    }
}
